package careers.nebula.ben.pojos;

/**
 * @author Ankit Verma
 *
 */
public class StringArrayPojo {	
	String s;
	String[] a;
	
	public StringArrayPojo(){
		
	}
	
	public StringArrayPojo(String s, String[] a){
		this.s = s;
		this.a = a;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public String[] getA() {
		return a;
	}

	public void setA(String[] a) {
		this.a = a;
	}
	
	
}
